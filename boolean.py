print("0 ->", bool(0)) 
print("0.0 ->", bool(0.0))
print("1 ->", bool(1))  
print("-5 ->", bool(-5))  
  
# strings  
print("'' ->", bool('')) 
print("'Tpoint Tech' ->", bool('Tpoint Tech'))  
  
# lists  
print("[] ->", bool([])) 
print("[11, 12, 13] ->", bool([11, 12, 13]))  
  
# tuples  
print("() ->", bool(()))   
print("(11, 12, 13) ->", bool((11, 12, 13)))  
  
# sets  
print("{} ->", bool({}))  
print("{11, 12, 13} ->", bool({11, 12, 13}))  
  
# None  
print("None ->", bool(None)) 